				<h2>Manfaat Buah-Buahan Bagi Kesehatan</h2>
			
			Manfaat buah-buahan dapat menjamin agar kesehatan Anda tetap optimal dalam jangka panjang. 
			Buah-buahan dapat menjadi makanan yang bermanfaat bagi Anda karena buah merupakan sumber alami dari vitamin dan mineral yang dibutuhkan untuk menunjang fungsi tubuh yang baik. 
			Kandungan serat yang banyak pada buah juga membantu dan mengoptimalkan fungsi pencernaan. Buah juga merupakan bagian penting dari diet yang sehat terutama bagi mereka yang ingin menurunkan berat badan.
			Selebihnya, manfaat buah-buahan yang lain ialah dapat menurunkan risiko Anda untuk terkena penyakit darah tinggi, kanker, penyakit jantung dan kencing manis. Buah juga dapat meningkatkan kondisi kesehatan kulit dan membantu pertumbuhan rambut. 
			Di bawah ini akan dibahas beberapa manfaat buah-buahan bagi kesehatan dan manfaat buah untuk diet.

            <p>1. Buah Dapat Memberikan Tambahan Energi Dengan Cepat</p>
			Buah akan menyediakan energy bagi tubuh yang dapat dipergunakan dengan cepat. Manfaat buah-buahan yang satu ini dapat kita gunakan sebagai penyedia energi yang cepat di tengah jadwal kita yang sibuk. Ini alasan mengapa banya atlet yang sering makan buah pada saat dan setelah beres latihan. Ini juga merupakan alasan mengapa buah merupakan menu yang bagus untuk diselipkan di makanan keseharian ibu hamil.
			
            <p>2. Mencegah Lebih Baik Daripada Mengobati</p>
			Jaman sekarang ini banyak masyarakat yang mengeluarkan uang lebih banyak untuk menjaga kesehatannya. Salah satunya ialah dengan membeli banyak suplemen dan multivitamin di apotek agar kesehatan tubuh tetap terjaga. Namun konsumsi vitamin dan nutrisi lainnya lebih baik didapatkan dari sumber yang alami. Buah merupakan sumber vitamin dan zat supplemental lainnya. Penelitian menyebutkan bahwa mengkonsumsi buah secara teratur dalam 10 â€“ 20 tahun dapat membuat tubuh lebih sehat.
            <p>3. Mencegah Berbagai Jenis Penyakit</p>
			Kombinasi kandungan falavanoid, antioksidan, mineral, vitamin, makronutrien serta mikronutrien membuat buah sangat bermanfaat bagi tubuh Anda. Manfaat buah-buahan yang satu ini dapat menurukan risiko Anda terkena stroke, tekanan darah tinggi, sembelit, kanker, penyakit jantung, kencing manis dan penyakit kronis lainnya. Misalnya buah pisang yang kaya akan kalium akan mencegah Anda menderita penyakit jantung, stroke dan tekanan darah tinggi bahkan mengurangi rasa cemas berlebih. Buah juga mengandung banyak air dan serat yang membantu fungsi pencernaan Anda.
            <p>4. Manfaat Buah Untuk Diet</p>
			Ketika Anda mulai memasukkan menu buah-buahan ke dalam menu harian Anda, Anda akan merasakan adanya penurunan berat badan atau berat badan Anda akan stabil pada angka yang normal. Banyak penelitian yang menyebutkan bahwa konsumsi buah dapat menurunkan berat badan. Hal ini dikarenakan orang yang gemar mengkonsumsi buah akan mengeliminasi makanan ringan yang tidak sehat di menu hariannya. Ditambah lagi kandungan air dari buah yang akan membantu proses detoksifikasi zat tak berguna di dalam tubuh. Secara umum, buah tidak mengandung lemak yang dapat menambah berat badan Anda. Namun jangan lupa untuk tetap berolahraga bila diet merupakan tujuan utama Anda.
            <p>5. Mengobati Jerawat</p>
			Jerawat merupakan salah satu kondisi kulit yang paling mengganggu dan menyebalkan. Dengan mengkonsumsi buah dalam jumlah yang cukup, Anda dapat mencegah dan mengatasi jerawat. Manfaat buah-buahan bagi kesehatan kulit Anda adalah menjaga kelembapan kulit dan memberikan kulit nutrisi yang baik. Beberapa buah juga memiliki efek tidak langsung yang dapat menekan jerawat. Contohnya apel yang mengandung banyak pectin akan membantu menyembuhkan konstipasi; dimana konstipasi merupakan salah satu pencetus terjadinya jerawat. Buah beri dapat membersihkan kulit sehingga melindungi kulit dari jerawat dan penyakit kulit lainnya. Buah lain yang dapat mencegah jerawat ialah anggur, pepaya dan lemon.
